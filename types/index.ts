import { JwtPayload } from "jsonwebtoken";

// 1️⃣ العيادة (Clinic) - الحساب الرئيسي للطبيب أو المركز الطبي
export interface Clinic {
  id: string;                           // معرف فريد للعيادة في قاعدة البيانات (UUID)
  name: string;                         // الاسم التجاري للعيادة كما يظهر للمرضى (مثال: "مركز الابتسامة لطب الأسنان")
  logo?: string;                        // رابط صورة الشعار (اختياري) - يستخدم في الصفحة العامة وفي كرت المريض
  subscriptionStatus: 'active' | 'expired' | 'trial'; // حالة الاشتراك في النظام: active=مدفوع وفعال | expired=منتهي | trial=فترة تجريبية مجانية
  createdAt: Date;                      // تاريخ ووقت إنشاء حساب العيادة في النظام
  address?: string;                     //عنوان العيادة
  doctorProfile: DoctorProfile;         // بيانات الطبيب الشخصية (تستخدم لعمل صفحة CV عامة)

  settings: {                           // إعدادات قابلة للتخصيص من قبل الطبيب
    defaultAppointmentDuration: number; // مدة الموعد الافتراضية بالدقائق (مثال: 30) - تستخدم عند إنشاء موعد جديد تلقائياً
    workingHours: WorkingHours[];       // مصفوفة تحتوي على أوقات الدوام لكل يوم من أيام الأسبوع
    primaryColor: string;               // 🎨 اللون الأساسي للهوية البصرية للعيادة (كود HEX مثل: "#0284c7") - يستخدم في الأزرار والعناوين
    secondaryColor: string;             // 🎨 اللون الثانوي للهوية البصرية (مثل: "#0ea5e9") - يستخدم للتأثيرات والخلفيات الفرعية
};
}

// 🧑‍⚕️ الملف الشخصي للطبيب (يظهر في صفحة CV العامة)
export interface DoctorProfile {
  fullName: string;          // الاسم الكامل للطبيب مع اللقب (مثال: "د. أحمد محمد علي")
  specialization: string;    // التخصص الدقيق (مثال: "دكتوراه في تركيبات وتجميل الأسنان")
  about: string;             // نبذة تعريفية عن الطبيب مكتوبة بشكل حر (تظهر في صفحة "عن الطبيب")
  education: string[];       // المؤهلات العلمية - مصفوفة نصوص، كل عنصر يمثل سطراً (مثال: ["بكالوريوس طب وجراحة الفم والأسنان - جامعة القاهرة", "ماجستير علاج الجذور - جامعة عين شمس"])
  experience: string[];      // الخبرات العملية - مصفوفة نصوص (مثال: ["10 سنوات في مجال زراعة الأسنان", "مدير مركز الابتسامة الطبي منذ 2018"])
  photo?: string;            // رابط الصورة الشخصية للطبيب (اختياري)
  contactEmail?: string;     // بريد إلكتروني مخصص لاستقبال استفسارات المرضى (قد يختلف عن بريد تسجيل الدخول)
  graduationYear: number;     // سنة التخرج
  university: string;         //الجامعة التي تخرج منها
}

// 📅 أوقات العمل لأيام الأسبوع
export interface WorkingHours {
  day: 0 | 1 | 2 | 3 | 4 | 5 | 6; // رقم اليوم: 0 = الأحد | 1 = الإثنين | 2 = الثلاثاء | 3 = الأربعاء | 4 = الخميس | 5 = الجمعة | 6 = السبت
  start: string;                  // وقت بداية الدوام بتنسيق 24 ساعة (مثال: "09:00")
  end: string;                    // وقت نهاية الدوام بتنسيق 24 ساعة (مثال: "17:00")
  isClosed: boolean;              // محدد لمعرفة ما إذا كانت العيادة مغلقة في هذا اليوم (مثلاً: يوم الجمعة تكون القيمة true)
}

