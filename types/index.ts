// 2️⃣ المريض (Patient) - الفرد الذي يتلقى العلاج في العيادة
export interface Patient {
  id: string;              // معرف فريد للمريض (UUID)
  clinicId: string;        // معرف العيادة التي ينتمي إليها هذا المريض (يستخدم لعزل بيانات العيادات عن بعضها)
  fullName: string;        // الاسم الثلاثي الكامل للمريض (مثال: "سارة خالد العتيبي")
  phone: string;           // رقم الجوال الأساسي للمريض (يستخدم للتواصل وإرسال رسائل تذكير الواتساب)
  gender: 'male' | 'female'; // جنس المريض: ذكر (male) أو أنثى (female)
  age?: number;       //عمر المريض
  notes?: string;          // ملاحظات عامة وسريعة عن المريض يكتبها الطبيب أو السكرتير (مثال: "تفضل مواعيد المساء - تخاف من الإبر")
  createdAt: Date;         // تاريخ ووقت إضافة المريض إلى قاعدة بيانات العيادة
}

