// 4️⃣ الحالة العلاجية (PatientCase) - الملف الطبي الكامل لمشكلة أسنان محددة
export interface PatientCase {
  id: string;                      // معرف فريد للحالة العلاجية
  patientId: string;               // معرف المريض صاحب هذه الحالة
  clinicId: string;                // معرف العيادة

  status: 'active' | 'completed' | 'waiting' | 'discontinued';
  // 'active'       = العلاج جارٍ والمريض يتردد على العيادة
  // 'completed'    = اكتملت جميع جلسات العلاج بنجاح
  // 'waiting'      = تم تشخيص الحالة ووضع خطة علاج لكن لم تبدأ الجلسات بعد (مثال: مريض ينتظر عروض سعر أو موافقته)
  // 'discontinued' = توقف العلاج بشكل مفاجئ (المريض اختفى أو قرر عدم الإكمال)

  // 🔍 التشخيص والمشكلة
  chiefComplaint: string;          // الشكوى الرئيسية كما وصفها المريض حرفياً (مثال: "ألم شديد عند شرب الماء البارد في الجهة اليمنى")
  diagnosis?: string;              // التشخيص النهائي من قبل الطبيب (مثال: "التهاب عصب حاد في الضرس رقم 46")
  toothNumber?: string[];          // جميع أرقام الأسنان المشمولة في هذه الحالة العلاجية (مثال: ["46", "47"])

  // 📅 التواريخ
  startDate: Date;                 // تاريخ فتح هذه الحالة (بداية التشخيص)
  completedDate?: Date;            // تاريخ إغلاق الحالة (يتم تعبئته تلقائياً عند تغيير الحالة إلى 'completed')

  // 💰 ملخص مالي (يُحدث تلقائياً عن طريق الكود)
  totalCost: number;               // المبلغ الإجمالي المستحق = مجموع sessionCost لكل الجلسات المرتبطة بالحالة
  totalPaid: number;               // المبلغ الإجمالي المدفوع فعلياً = مجموع sessionCost للجلسات التي isPaid = true

  // 🔗 العلاقة (تستخدم لجلب البيانات من قاعدة البيانات)
  sessions: Session[];             // مصفوفة تحتوي على جميع جلسات هذه الحالة (مرتبة تصاعدياً حسب التاريخ)
}
export type ClinicEmployeeRole = 'admin'|'manager'|'employee';
export interface ClinicEmployeeJwt extends JwtPayload {
  id: string;
  clinicId: string;
  role : ClinicEmployeeRole;
  subscriptionStatus : 'active' | 'expired' | 'trial';
}